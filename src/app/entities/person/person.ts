
export class Person {
	
	public id: number;
	public fname: string;
	public lname: string;
	public email: string;
	public password: string;
	
	constructor(fname: string, lname: string, email: string, password: string) {
		this.fname = fname;
		this.lname = lname;
		this.email = email;
		this.password = password;
	}
	
}





